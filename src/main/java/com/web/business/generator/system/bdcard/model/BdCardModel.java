package com.web.business.generator.system.bdcard.model;

public class BdCardModel {
	
	
	 	private String cardId;

	    private String cardNum;//卡号
	    
	    private String broadcastNum;//通播地址
	    
	    private String commFre;//通信频度

	    private String commGrade;//通信等级
	    
	    private String remainTime;//到期时间
	    
	    private  String isExceed;//是否超期 0标识无 、 1标识超期， 2未开卡
	    
	    private  String status;//状态  0为正常 1为无效
	    
	    private String cardRemark;

		public String getCardId() {
			return cardId;
		}

		public void setCardId(String cardId) {
			this.cardId = cardId;
		}

		public String getCardNum() {
			return cardNum;
		}

		public void setCardNum(String cardNum) {
			this.cardNum = cardNum;
		}

		public String getBroadcastNum() {
			return broadcastNum;
		}

		public void setBroadcastNum(String broadcastNum) {
			this.broadcastNum = broadcastNum;
		}

		public String getCommFre() {
			return commFre;
		}

		public void setCommFre(String commFre) {
			this.commFre = commFre;
		}

		public String getCommGrade() {
			return commGrade;
		}

		public void setCommGrade(String commGrade) {
			this.commGrade = commGrade;
		}

		public String getRemainTime() {
			return remainTime;
		}

		public void setRemainTime(String remainTime) {
			this.remainTime = remainTime;
		}

		public String getIsExceed() {
			return isExceed;
		}

		public void setIsExceed(String isExceed) {
			this.isExceed = isExceed;
		}

		public String getStatus() {
			return status;
		}

		public void setStatus(String status) {
			this.status = status;
		}

		public String getCardRemark() {
			return cardRemark;
		}

		public void setCardRemark(String cardRemark) {
			this.cardRemark = cardRemark;
		}

}
