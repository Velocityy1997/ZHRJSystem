package com.web.business.generator.util.tree;

/**
 * Ztree节点
 * @author cll
 * @date 2019年8月9日
 *
 */
public class TreeDataInfo {

	private String id;
	private String card;//卡号
	private String terminalId;//终端id
	
	private String name;
	private boolean isParent;	
	private String pId;		
	private String icon;
	private String iconOpen;
	private String iconClose;
	private String type;//节点类型      根节点：root,下属：inzone,非下属：outzone
	private Long sonNodesSum; //下属节点总数
	
	public String getTerminalId() {
		return terminalId;
	}
	public void setTerminalId(String terminalId) {
		this.terminalId = terminalId;
	}
	public String getCard() {
		return card;
	}
	public void setCard(String card) {
		this.card = card;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public boolean isParent() {
		return isParent;
	}
	public void setParent(boolean isParent) {
		this.isParent = isParent;
	}
	public String getpId() {
		return pId;
	}
	public void setpId(String pId) {
		this.pId = pId;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	public String getIconOpen() {
		return iconOpen;
	}
	public void setIconOpen(String iconOpen) {
		this.iconOpen = iconOpen;
	}
	public String getIconClose() {
		return iconClose;
	}
	public void setIconClose(String iconClose) {
		this.iconClose = iconClose;
	}
	public Long getSonNodesSum() {
		return sonNodesSum;
	}
	public void setSonNodesSum(Long sonNodesSum) {
		this.sonNodesSum = sonNodesSum;
	}
	@Override
	public String toString() {
		return "TreeDataInfo [id=" + id + ", name=" + name + ", pId=" + pId + ", sonNodesSum=" + sonNodesSum + "]";
	}
	
	
	
	
	
}
